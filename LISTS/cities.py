cities = ["Nairobi", "Harare", "Kampala", "Lagos", "Dodoma"]

# **********PRINT LIST IN ORIGINAL ORDER**********
print(cities)

# ***********TEMPORARILY SORT LIST***********
sorted(cities)
print(cities)

# ***********REVERSE LIST ORDER***********
print(cities)
cities.reverse()
print(cities)

# ***********PERMANENTLY SORT LIST*********
cities.sort()
print(cities)

# **********PRINT STRING LENGTH**********
print(len(cities))
